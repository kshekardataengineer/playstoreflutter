  String name;
  String dob;
  String city;
  String state;
  String country;
  String occupation;
  String role;

  UserProfile({
    required this.name,
    required this.dob,
    required this.city,
    required this.state,
    required this.country,
    required this.occupation,
    required this.role,
  });
}
